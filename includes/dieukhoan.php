
<div class="polisy">
        <h2 class="policy-title">Điều khoản dịch vụ bảo hành sản phẩm</h2>
        <ol class="polisied">
            <h3>Điều kiện được bảo hành</h3>
            <li>Sản phẩm bắt đầu được tính bảo hành kể từ ngày xuất hóa đơn từ đại lý. Khách hàng có nghĩa vụ lưu giữ
                hóa đơn mua hàng (hoặc bằng chứng mua hàng) để bảo vệ quyền lợi của mình.</li>
            <li>Để biết cách xác định thời hạn bảo hành của các sản phẩm khác nhau, vui lòng tham khảo hướng dẫn tìm số
                sê-ri trên trang sản phẩm.</li>
            <li>Thời gian bảo hành được tính theo số sê-ri của sản phẩm nếu khách hàng không giữ / không có hóa đơn mua
                hàng (hoặc chứng từ mua hàng) của đại lý.</li>
            <li>Thời gian bảo hành thay đổi theo các quốc gia / khu vực khác nhau. Vui lòng liên hệ với đại lý nơi bạn
                mua sản phẩm.</li>
            <li>Các hư hỏng và trục trặc do sử dụng không đúng cách và tháo lắp không đúng cách sẽ không được bảo hành.
            </li>
            <li>Dịch vụ Bảo hành của FPI service không bao gồm các thiệt hại do người dùng hoặc các bên không được ủy
                quyền chính hãng tháo gỡ sản phẩm, do người dùng tự tháo / lắp sai (không bao gồm các sản phẩm cầm tay).
            </li>
            <li>Khách hàng tự ý tháo nhãn bảo hành ban đầu của hãng, nhãn bảo hành không đồng nhất với kiểu máy / thông
                số kỹ thuật của sản phẩm, không xác định được số sê-ri do nhãn bị xóa và mòn do tác động, hoặc các sản
                phẩm không có nhãn số sê-ri được đính kèm. Đối với tất cả các trường hợp trên, FPI service sẽ chấm dứt
                quyền bảo hành sản phẩm và dịch vụ sửa chữa sẽ không được cung cấp.</li>
        </ol>

        <ol class="non-policy">
            <h3>Các trường hợp tính phí sửa chữa</h3>
            <li>Các thiệt hại do thiên tai, ngoại lực không thể khắc phục được hoặc do sơ suất của con người thao tác
                không đúng cách</li>
            <li>Các hư hỏng và trục trặc là do người dùng tự tháo rời, thay đổi thông số kỹ thuật mà không có sự cho
                phép chính thức hoặc sử dụng các phụ kiện chính hãng không phải của ASU</li>
            <li>Sự cố do cài đặt phần mềm trái phép và thay đổi cài đặt.</li>
            <li>Các lỗi và sự cố do máy tính bị nhiễm vi rút gây ra.</li>
            <li>Nhãn Bảo hành bị hỏng hoặc không thể nhận dạng. Để bảo vệ quyền lợi của bạn, vui lòng không tháo nhãn
                bảo hành ban đầu của máy (có số sê-ri).</li>
            <li>Cáp và mạch điện tiếp xúc với hơi ẩm hoặc tiếp xúc với chất lỏng, do đó gây ra quá trình oxy hóa và rỉ
                sét nghiêm trọng.</li>
            <li>Sử dụng hoặc thao tác sản phẩm không đúng cách khác.</li>
        </ol>

